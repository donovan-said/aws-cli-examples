#!/bin/bash

########################################################################################################################
################################################## GET Secret VALUE ####################################################
########################################################################################################################

SECRET=$(aws secretsmanager get-secret-value \
  --secret-id "INSERT_SECRET_NAME" \
  --region eu-west-2 \
  | jq --raw-output .SecretString | jq -r ."SECRET")

########################################################################################################################
################################################ PUT/UPDATE Secret VALUE ###############################################
########################################################################################################################

aws secretsmanager update-secret \
    --secret-id "INSERT_SECRET_NAME" \
    --secret-string '{"KEY": "VALUE"}' \
    --profile "$AWS_PROFILE" \
    --region "$REGION" 1> /dev/null