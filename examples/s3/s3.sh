#!/bin/bash

########################################################################################################################
############################################## Check if S3 Bucket Exists ###############################################
########################################################################################################################

aws s3api head-bucket --bucket "INSERT_BUCKET_NAME" --profile "$AWS_PROFILE" 2>&1

########################################################################################################################
############################################### Create S3 Bucket #######################################################
########################################################################################################################

aws s3api create-bucket \
  --bucket "INSERT_BUCKET_NAME" \
  --profile "$AWS_PROFILE" \
  --region eu-west-2 \
  --create-bucket-configuration LocationConstraint="$REGION" 1> /dev/null

########################################################################################################################
############################################### Upload to S3 Bucket ####################################################
########################################################################################################################

aws s3 cp "INSERT_SOURCE" s3://"INSERT_DESTINATION" --profile "$AWS_PROFILE"